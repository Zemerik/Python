# Slicing
numbers = [0, 1, 2, 3, 4, 5]
print("Middle elements:", numbers[2:4])
print("Last two:", numbers[-2:])

# List comprehension
squares = [x**2 for x in range(1, 6)]
print(f"Squares: {squares}")

# Combining lists
list1 = [1, 2, 3]
list2 = [4, 5]
combined = list1 + list2
print(f"Combined: {combined}")